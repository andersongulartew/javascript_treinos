var cor = "green"

switch(cor){
    case "green":
        console.log("verde")
        break;

        case "blue":
            console.log("azul")
            break;

        case "white":
            console.log("branco")
            break;

            case "red":
            console.log("vermelho")
            break;

            case "black":
            console.log("preto")
            break;

        
            default:
                console.log("esta cor esta incorreta")

}
